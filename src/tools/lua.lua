-- 根据年龄和性别生成名字的函数
-- @param age 年龄
-- @param gender 性别 ('M' 表示男性, 'F' 表示女性)
-- @return string 生成的名字
function generateName(age, gender)
    -- 将年龄和性别组合成字符串进行hash
    local input = age .. gender
    local hash = 0
    
    -- 简单的字符串hash算法
    for i = 1, #input do
        hash = hash * 31 + string.byte(input, i)
    end
    
    -- 确保hash值为正数
    hash = math.abs(hash)
    
    -- 使用hash值选择名字列表中的一个名字
    local index = (hash % #nameList) + 1
    
